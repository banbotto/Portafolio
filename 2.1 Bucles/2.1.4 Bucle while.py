
#Creando un contador que va a ir sumandose
contador = 0

#Mientras que la condicion se cumpla, el bucle se va a ejecutar (vuelta tras vuelta se verifica la condición)
while contador < 10:
    print(contador)
    contador+=1 

#Al revés incluye el bucle el último digito de la condición
while contador < 10:
    contador+=1
    print(contador)
    
